// 初始化字典，提取选用表情的Lab平均色
window.initEmojiDictionary = function(theme = 'all') {
    const paletteInfo = document.getElementById('paletteInfo');
    if (paletteInfo) paletteInfo.textContent = `⏳ 正在解析画笔库...`;

    window.emojiDict = [];
    const emojisToUse = palettes[theme] || palettes.all;
    
    const offscreenCanvas = document.createElement('canvas');
    const offCtx = offscreenCanvas.getContext('2d', { willReadFrequently: true });
    offscreenCanvas.width = 32;
    offscreenCanvas.height = 32;
    
    const emojiArray = Array.from(emojisToUse);

    emojiArray.forEach(emoji => {
        offCtx.clearRect(0, 0, 32, 32); 
        offCtx.font = '24px "Segoe UI Emoji", "Apple Color Emoji", "Noto Color Emoji", sans-serif';
        offCtx.textAlign = "center";
        offCtx.textBaseline = "middle";
        offCtx.fillText(emoji, 16, 16);

        const imgData = offCtx.getImageData(0, 0, 32, 32).data;
        let rSum = 0, gSum = 0, bSum = 0, count = 0;

        for (let i = 0; i < imgData.length; i += 4) {
            const alpha = imgData[i + 3];
            if (alpha > 200) {
                rSum += imgData[i];
                gSum += imgData[i + 1];
                bSum += imgData[i + 2];
                count++;
            }
        }

        if (count > 0) {
            const avgR = Math.round(rSum / count);
            const avgG = Math.round(gSum / count);
            const avgB = Math.round(bSum / count);
            window.emojiDict.push({
                emoji: emoji,
                rgb: [avgR, avgG, avgB],
                lab: rgbToLab(avgR, avgG, avgB) // 调用 color.js 中的方法
            });
        }
    });

    // 解析完成后更新提示
    if (paletteInfo) {
        paletteInfo.textContent = `✅ 当前画笔库提供 ${window.emojiDict.length} 种 Emoji 色板`;
    }
}

// 核心匹配算法
window.findClosestEmoji = function(r, g, b) {
    if (window.emojiDict.length === 0) return '⬜';
    const targetLab = rgbToLab(r, g, b); 
    let closestEmoji = window.emojiDict[0].emoji;
    let minDistance = Infinity;

    for (const item of window.emojiDict) {
        const dist = colorDistanceLabSq(targetLab, item.lab); 
        if (dist < minDistance) {
            minDistance = dist;
            closestEmoji = item.emoji;
        }
    }
    return closestEmoji;
}